class Gallery extends Component {
  state = {
    selectedImage: {
      selectedImageUrl: imagesList[0].imageUrl,
      selectedImageAltText: imagesList[0].imageAltText,
    },
    selectedImageId: imagesList[0].id,
  }

  updateDisplayImage = (imageUrl, imageAltText, id) => {
    this.setState({
      selectedImage: {
        selectedImageUrl: imageUrl,
        selectedImageAltText: imageAltText,
      },
      selectedImageId: id,
    })
  }

  render() {
    const {selectedImage, selectedImageId} = this.state
    const {selectedImageUrl, selectedImageAltText} = selectedImage
    return (
      <div className="app-container">
        <div className="gallery-container">
          <img
            src={selectedImageUrl}
            alt={selectedImageAltText}
            className="image"
          />
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul className="thumbnails-container">
            {imagesList.map(imageDetails => (
              <ThumbnailItem
                key={imageDetails.id}
                imageDetails={imageDetails}
                isSelected={selectedImageId === imageDetails.id}
                updateDisplayImage={this.updateDisplayImage}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
